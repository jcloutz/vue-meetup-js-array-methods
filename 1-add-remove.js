// Add to end of the array with array.push()
const arr1 = [1, 2, 3]
arr1.push(4)
console.log(arr1)

// Remove from end of array with array.pop()
const arr2 = [1, 2, 3]
arr2.pop()
console.log(arr2)

// Remove from beginning of array with array.shift()
const arr3 = [1, 2, 3]
arr3.shift()
console.log(arr3)

// Add to beginning of array with array.unshift()
const arr4 = [1, 2, 3]
arr4.unshift(4)
console.log(arr4)